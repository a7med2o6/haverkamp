  function applyLang(lang) {
    /* text / html nodes */
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (T[lang] && T[lang][key] !== undefined) el.innerHTML = T[lang][key];
    });

    /* placeholders */
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      if (PLACEHOLDERS[lang] && PLACEHOLDERS[lang][key]) el.placeholder = PLACEHOLDERS[lang][key];
    });

    /* contact form select */
    var sel = document.getElementById('cf-service');
    if (sel && FORM_OPTS[lang]) {
      Array.from(sel.options).forEach(function (opt, i) {
        if (FORM_OPTS[lang][i]) opt.text = FORM_OPTS[lang][i];
      });
    }

    /* document direction + lang */
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'en' ? 'ltr' : 'rtl');

    /* lang button label */
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.textContent = lang === 'ar' ? '🇬🇧' : '🇰🇼';
    });

    localStorage.setItem(LANG_KEY, lang);
  }

